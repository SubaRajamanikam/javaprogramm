package Day5;

public class Condition3 {
	public static void main(String[] args) {
	int[] a= {10,20,50,55,65};
	int e=0,d=0;
	for( int i=0;i<a.length;i++) {
		if(a[i]%2==0) {
		e=e+1;
			
		}
		else {
		d=d+1;
		}
	}
	System.out.println("even value="+e+","+"odd value="+d);
	}
}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
